package com.kickmogu.yodobashi.community.common.exception;

public class UnMatchTargetException extends YcComException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7510230240703813676L;

	public UnMatchTargetException() {
		super();
	}

	public UnMatchTargetException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnMatchTargetException(String message) {
		super(message);
	}

	public UnMatchTargetException(Throwable cause) {
		super(cause);
	}
	
}
